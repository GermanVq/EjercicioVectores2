    private void cmdMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdMostrarActionPerformed
      int contpares=0,contimpares=0,contprimo=0;
      
        for (int i = 0; i < v.length; i++) {
            
            if(v[i]%2==0){
                contpares=contpares+1;
            }
            else if(v[i]%2!=0){
                contimpares=contimpares+1;
            }
           
        }
        for (int p = 1; p < v.length; p++) {
             int divisor=2;
             
             while(v[p]%divisor!=0){
                 divisor=divisor+1;
             }
             if(divisor==v[p]){
                 contprimo=contprimo+1;
             }
        
        }
        
        String contPar=String.valueOf(contpares);
        txtPares.setText(contPar);
        String contImpar=String.valueOf(contimpares);
        txtImpares.setText(contImpar);
        String cont_primos=String.valueOf(contprimo);
        txtPrimos.setText(cont_primos);
        
    }//GEN-LAST:event_cmdMostrarActionPerformed

